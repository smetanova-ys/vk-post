package ru.netology.domain;

public class PostViewsInfo {
    private String viewsIcon;
    private long viewsCounter;

    public String getViewsIcon() {
        return viewsIcon;
    }

    public void setViewsIcon(String viewsIcon) {
        this.viewsIcon = viewsIcon;
    }

    public long getViewsCounter() {
        return viewsCounter;
    }

    public void setViewsCounter(long viewsCounter) {
        this.viewsCounter = viewsCounter;
    }
}
